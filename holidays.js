function getOfficialHolidays(year){

    if(HOLIDAY_DATA[year]){

        return HOLIDAY_DATA[year];

    }

    return [

        `${year}-01-01`,
        `${year}-04-23`,
        `${year}-05-01`,
        `${year}-05-19`,
        `${year}-07-15`,
        `${year}-08-30`,
        `${year}-10-29`

    ];

}

function isOfficialHoliday(dateString){

    const year=parseInt(dateString.substring(0,4));

    return getOfficialHolidays(year).includes(dateString);

}

function getHolidayCount(year,month){

    return getOfficialHolidays(year).filter(x=>{

        const d=new Date(x);

        return d.getMonth()==month;

    }).length;

}